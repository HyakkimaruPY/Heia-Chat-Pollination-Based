/* ─── I18n ─────────────────────────────────────────── */
const I18n = {
  detect() {
    const supported = Object.keys(STRINGS);
    const preferred = navigator.languages?.length ? [...navigator.languages] : [navigator.language||'en'];
    for (const p of preferred) {
      const base = p.split('-')[0].toLowerCase();
      if (supported.includes(base)) return base;
    }
    return 'en';
  },
  t(k) { return (state.translations[k] ?? STRINGS.en[k]) || k; },
  apply() {
    const map = {
      'txt-welcome':'welcome',        'txt-welcome-desc':'welcomeDesc',
      'txt-enter':'enter',            'txt-get-key':'getKey',
      'txt-key-error':'keyError',     'txt-new-chat':'newChat',
      'txt-chats-label':'chatsLabel', 'txt-memory':'memory',
      'txt-settings':'settings',      'txt-input-hint':'inputHint',
      'txt-settings-title':'settingsTitle', 'txt-tab-keys':'tabKeys',
      'txt-tab-appearance':'tabAppearance', 'txt-memory-title':'memoryTitle',
      'txt-clear-memory':'clearMemory','txt-close':'close',
      'txt-expired-title':'expiredTitle','txt-expired-desc':'expiredDesc',
      'txt-renew':'renew',            'txt-scroll-down':'scrollDown',
      'sb-pollen-built':'poweredBy',  'txt-key-pollen-built':'poweredBy',
      'txt-input-pollen-built':'poweredBy',
      'txt-help':'help',              'txt-help-title':'helpTitle',
      'txt-help-close':'close',
      'txt-model-picker-title':'modelPickerTitle',
    };
    for (const [id,k] of Object.entries(map)) {
      const el = document.getElementById(id) || document.querySelector('.'+id);
      if (el) el.textContent = I18n.t(k);
    }
    const mi = document.getElementById('msg-input');
    if (mi) mi.placeholder = I18n.t('msgPlaceholder');
    const st = document.getElementById('stream-status-text');
    if (st) st.textContent = I18n.t('streamText');
    const bb = document.getElementById('brain-btn');
    if (bb) bb.title = I18n.t('memory');
  },
  load() {
    state.lang = I18n.detect();
    state.translations = STRINGS[state.lang] || STRINGS.en;
    I18n.apply();
  }
};
